import sortCharsHealth from '../src/app';

test('Sort array by health', () => {
  const characters = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const expected = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];
  const recieved = sortCharsHealth(characters);
  expect(recieved).toEqual(expected);
});
